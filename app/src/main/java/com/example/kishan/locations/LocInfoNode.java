package com.example.kishan.locations;

public class LocInfoNode
{
    public String name;
    public String time;
    public String coordinates;
    public String address;

    public LocInfoNode(String name, String time, String coordinates, String address)
    {
        this.name = name;
        this.time= time;
        this.coordinates = coordinates;
        this.address = address;
    }
}
